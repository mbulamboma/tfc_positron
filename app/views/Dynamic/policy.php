<h2 class="mt-5"> 0. Introdution</h2>
<p> La protection des données personnelles de votre vie privée est l’une des premières priorités de CHIDA ! et cela ne changera pas.
        A cet effet, nous avons mis au point une politique de confidentialité qui couvre notre manière de collecter, utiliser, divulguer, transférer et stocker vos informations.
    Veuillez disposer de votre temps pour parcourir les lignes qui suivent, et vous familiariser avec nos pratiques de confidentialité et nous contacter pour toutes vos préoccupations.
   </p> 

<p> Toute activité sur CHIDA ! subséquente à la lecture de cet avis de confidentialité revient à approuver les pratiques qui y sont décrites, 
        et assumer toutes les retombées y associées.
        </p> 

<p> Dans le présent document, les termes ci-après « vous » et « utilisateur » désignent toute personne accédant la plateforme mobile CHIDA !. 
        Les termes « nous », « notre application », et « notre plateforme »  désignent CHIDA !.
            </p>

<p> La protection de votre vie privée est une obligation incontournable pour nous. 
        Ainsi nous ne recueillons des données personnelles que dans le seul but de vous offrir la possibilité d’une utilisation optimale de notre application.
            </p>

<p> En soumettant des données personnelles sur notre application, vous acceptez l'utilisation de vos données 
    personnelles dans les conditions prévues dans la présente politique de confidentialité.
            </p>

<p> Il est donc impérieux pour vous de comprendre que sans la soumission de certaines de vos informations personnelles, 
        il vous sera impossible de jouir pleinement des bénéfices que procure notre application.
            </p>

<h2> 1. Récupération des données personnelles de nos utilisateurs </h2>

<p> Lors de votre inscription, nous recueillons des données personnelles telles que votre numéro de téléphone mobile (avec l’indicatif de votre pays), 
        votre nom de profil, et votre localisation (plus précisément votre ville ou province). 
            </p>

<p> En outre, vous nous fournissez régulièrement des numéros de téléphone dans le carnet d'adresses de votre téléphone, 
        y compris ceux des personnes qui utilisent nos Services et de vos autres contacts. 
            Vous pouvez également ajouter d'autres informations dans votre compte, comme une photo de profil et des informations 
            à votre sujet (vos compétences et réalisations par exemple).
            </p>

<p> Nous avons en effet besoin de ces données personnelles, notamment pour vous permettre d’entrer en contact avec
        vos partenaires (clients, acheteurs, amateurs) au sein de notre application, faciliter votre localisation en cas de commande, 
            d’achat ou de livraison d’un produit tout comme pour le choix d’un service donné, et surtout détecter, examiner, 
            prévenir et traiter la fraude et toute autre activité illégale ou les problèmes techniques 
            ou de sécurité dans le but d’instaurer un climat d’affaires sûr et sécurisé.
            </p>

<h2> 2. Utilisation des données personnelles de nos utilisateurs </h2>

<p> Nous stockons et traitons vos données personnelles sur des serveurs protégés.  
   Ces stockage et traitement font l’objet d’une constante vérification dans le seul but du respect des règles de confidentialité détaillées ici. 
       </p>

<p> Nous pouvons transmettre vos coordonnées personnelles à des sociétés tierces afin d'effectuer la livraison de la commande. 
    Nous pouvons également utiliser vos données personnelles afin de détecter des manquements 
    aux Conditions Générales d'Utilisation, et vous envoyer des données personnelles sur notre application.
       </p>

<p> En utilisant notre application, vous acceptez que nous vous montrions des publicités ; 
        toutefois, celles-ci sont soigneusement triées et placées de manière à ne pas perturber vos activités sur notre plateforme. 
       </p>

<p> Vos données personnelles sont anonymes et ne sont utilisées qu’à des fins statistiques.
    Nous ne partageons aucune de vos données personnelles avec des sociétés tierces sans votre consentement explicite. 
    De la même manière, nous ne vendons ni ne louons vos données personnelles à des sociétés tierces pour des fins de marketing sans votre consentement explicite.
   </p>

<h2>3. Exactitude des données personnelles de nos utilisateurs</h2>

<p> Après la lecture de cet avis de confidentialité, vous devez vous engager qu’ à ne fournir que des données personnelles exactes vous concernant et à mettre à jour régulièrement ces dernières.
    Vous certifiez que vous êtes le propriétaire du contenu que vous nous soumettez et que ce dernier ne porte atteinte à quiconque et respecte les principes de propriété intellectuelle. 
    Vous certifiez également que, à votre connaissance, aucune action, poursuite, procédure, ou enquête n’a été engagée à votre encontre à propos du contenu que vous soumettez.
   </p>

<p> A défaut de ce qui précède, vous subirez seul(e) toutes les conséquences qui y découlent.
   </p>

<h2> 4.Sécurité </h2>

<p> Vous avez le contrôle de vos informations (publications, etc.) et vous pouvez les supprimer. 
   Vous êtes propriétaire des informations que vous partagez sur Chida !. En d'autres mots, vous décidez quoi partager avec les autres utilisateurs sur Chida ! 
   et vous pouvez toujours changer d’avis. C’est pourquoi nous vous donnons les moyens de supprimer n'importe quelle de vos publications.
   Nous le supprimons de votre bureau et de nos serveurs. Nous vous recommandons de ne pas envoyer toutes vos informations de paiement 
   dans vos communications par E-mail ou tout autre moyen de communication avec notre Service Client ou d’autres utilisateurs.
           </p>

<h2> 5. Modification de la présente Politique de Confidentialité </h2>

<p> Dans un monde qui change et où les technologies évoluent sans cesse, cette politique de confidentialité peut être sujette à modifications. 
   Nous nous réservons le droit de modifier la présente Politique de confidentialité à tout moment. 
       </p>

<p> Toutefois si nous modifions la présente politique d’une manière que nous considérons, à notre seule discrétion, comme substantielle, nous vous en informerons.
       Nous vous invitons néanmoins à consulter la dernière version de cette politique sur le « store » de téléchargement de l’application.
   </p>	

<p> En continuant d’accéder aux services ou en les utilisant après l’entrée en vigueur de ces changements, 
       vous acceptez d’être lié aux conditions énoncées dans la nouvelle Politique de confidentialité.
           </p>

